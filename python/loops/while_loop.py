'''
while expression:
    statement(s)
'''
n = 5
while n > 0:
    print(n)
    n = n - 1
    print('Blastoff!')

'''
while condition:
     # execute these statements
else:
     # execute these statements
'''
count = 0
while (count < 3):
	count = count + 1
	print("Hello world")
else:
	print("In Else Block")

#INFINITE WHILE LOOP
count = 0
while (count == 0):
	print("Hello Geek")

